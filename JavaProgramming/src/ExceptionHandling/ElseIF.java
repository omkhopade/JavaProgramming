package ExceptionHandling;

public class ElseIF {

	public static void main(String[] args) {

		int num=9;
		
		if(num==9)
		{
			System.out.println("Number is equal");
		}
		
		else if(num==10)
		{
			System.out.println("number is not equal ");
		}
		
		else
		{
			System.out.println("Wrong number ");
		}
		
	}

}
